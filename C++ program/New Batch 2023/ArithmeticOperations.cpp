#include<iostream>
using namespace std;
int main(){
    int a,b;
    cout<<"Enter 2 values: ";
    cin>>a>>b;
    // a + b = (a+b)
    cout<<a<<" + "<<b<<" = "<<a+b<<endl;
    cout<<a<<" - "<<b<<" = "<<a-b<<endl;
    cout<<a<<" * "<<b<<" = "<<a*b<<endl;
    cout<<a<<" / "<<b<<" = "<<a/b<<endl;
    cout<<a<<" % "<<b<<" = "<<a%b<<endl;
    return 0;
}